// Corner offsets for a unit cube (Three.js MarchingCubes convention)
// Bit i of cubeindex → corner i is below threshold
const CX = [0, 1, 1, 0, 0, 1, 1, 0]
const CY = [0, 0, 1, 1, 0, 0, 1, 1]
const CZ = [0, 0, 0, 0, 1, 1, 1, 1]

// Each edge connects two corners [a, b]
const EDGE_CORNERS = [
  [0,1],[1,2],[2,3],[3,0],
  [4,5],[5,6],[6,7],[7,4],
  [0,4],[1,5],[2,6],[3,7],
] as const

export interface SolverBall {
  x: number; y: number; z: number
  /** Effective radius: controls how large the blob appears */
  r: number
  negative: boolean
}

/**
 * Extracts an isosurface from the metaball scalar field using Marching Cubes.
 * Returns a BufferGeometry with smooth gradient-interpolated normals.
 */
export function buildMetaballGeometry(
  balls: SolverBall[],
  threshold: number,
  resolution: number,
  extent: number,
): BufferGeometry {
  const N    = resolution
  const step = (extent * 2) / N
  const M    = N + 1   // grid points per axis

  // ── Sample scalar field ────────────────────────────────────────────────────
  const field = new Float32Array(M * M * M)
  const idx = (xi: number, yi: number, zi: number) => xi * M * M + yi * M + zi

  for (let xi = 0; xi <= N; xi++) {
    const wx = -extent + xi * step
    for (let yi = 0; yi <= N; yi++) {
      const wy = -extent + yi * step
      for (let zi = 0; zi <= N; zi++) {
        const wz = -extent + zi * step
        let v = 0
        for (const b of balls) {
          const dx = wx - b.x, dy = wy - b.y, dz = wz - b.z
          const d2 = dx*dx + dy*dy + dz*dz + 1e-8
          v += b.negative ? -(b.r * b.r / d2) : (b.r * b.r / d2)
        }
        field[idx(xi, yi, zi)] = v
      }
    }
  }

  // ── Gradient helper (finite differences → outward surface normal) ──────────
  const gx = (xi: number, yi: number, zi: number) => {
    const x0 = xi > 0 ? xi - 1 : xi
    const x1 = xi < N ? xi + 1 : xi
    // negative: gradient points inward (high→low field = outward)
    return -(field[idx(x1,yi,zi)] - field[idx(x0,yi,zi)]) / ((x1 - x0) * step)
  }
  const gy = (xi: number, yi: number, zi: number) => {
    const y0 = yi > 0 ? yi - 1 : yi
    const y1 = yi < N ? yi + 1 : yi
    return -(field[idx(xi,y1,zi)] - field[idx(xi,y0,zi)]) / ((y1 - y0) * step)
  }
  const gz = (xi: number, yi: number, zi: number) => {
    const z0 = zi > 0 ? zi - 1 : zi
    const z1 = zi < N ? zi + 1 : zi
    return -(field[idx(xi,yi,z1)] - field[idx(xi,yi,z0)]) / ((z1 - z0) * step)
  }

  // ── March the cubes ────────────────────────────────────────────────────────
  const positions: number[] = []
  const normals:   number[] = []

  // Reusable per-edge vertex/normal scratch arrays
  const vx = new Float32Array(12), vy = new Float32Array(12), vz = new Float32Array(12)
  const nx = new Float32Array(12), ny = new Float32Array(12), nz = new Float32Array(12)

  for (let xi = 0; xi < N; xi++) {
    for (let yi = 0; yi < N; yi++) {
      for (let zi = 0; zi < N; zi++) {
        // Build cubeindex: bit set when corner field < threshold (= outside blob)
        let cubeIdx = 0
        const vals = [
          field[idx(xi,       yi,       zi      )],
          field[idx(xi+CX[1], yi+CY[1], zi+CZ[1])],
          field[idx(xi+CX[2], yi+CY[2], zi+CZ[2])],
          field[idx(xi+CX[3], yi+CY[3], zi+CZ[3])],
          field[idx(xi+CX[4], yi+CY[4], zi+CZ[4])],
          field[idx(xi+CX[5], yi+CY[5], zi+CZ[5])],
          field[idx(xi+CX[6], yi+CY[6], zi+CZ[6])],
          field[idx(xi+CX[7], yi+CY[7], zi+CZ[7])],
        ]
        for (let c = 0; c < 8; c++) if (vals[c] < threshold) cubeIdx |= (1 << c)

        const bits = edgeTable[cubeIdx]
        if (bits === 0) continue

        // Interpolate edge vertices and normals
        for (let e = 0; e < 12; e++) {
          if (!(bits & (1 << e))) continue
          const [a, b] = EDGE_CORNERS[e]
          const va = vals[a], vb = vals[b]
          const t = (threshold - va) / (vb - va)
          const ax = xi + CX[a], ay = yi + CY[a], az = zi + CZ[a]
          const bx = xi + CX[b], by = yi + CY[b], bz = zi + CZ[b]
          vx[e] = -extent + (ax + t * (bx - ax)) * step
          vy[e] = -extent + (ay + t * (by - ay)) * step
          vz[e] = -extent + (az + t * (bz - az)) * step
          // Interpolate gradient-based outward normals
          const ngx = gx(ax,ay,az) + t*(gx(bx,by,bz)-gx(ax,ay,az))
          const ngy = gy(ax,ay,az) + t*(gy(bx,by,bz)-gy(ax,ay,az))
          const ngz = gz(ax,ay,az) + t*(gz(bx,by,bz)-gz(ax,ay,az))
          const len = Math.sqrt(ngx*ngx + ngy*ngy + ngz*ngz) || 1
          nx[e] = ngx / len
          ny[e] = ngy / len
          nz[e] = ngz / len
        }

        // Emit triangles from triTable
        const base = cubeIdx << 4
        let i = 0
        while (triTable[base + i] !== -1) {
          const e0 = triTable[base + i]
          const e1 = triTable[base + i + 1]
          const e2 = triTable[base + i + 2]
          positions.push(vx[e0],vy[e0],vz[e0], vx[e1],vy[e1],vz[e1], vx[e2],vy[e2],vz[e2])
          normals.push(  nx[e0],ny[e0],nz[e0], nx[e1],ny[e1],nz[e1], nx[e2],ny[e2],nz[e2])
          i += 3
        }
      }
    }
  }

  const geo = new BufferGeometry()
  geo.setAttribute('position', new Float32BufferAttribute(new Float32Array(positions), 3))
  geo.setAttribute('normal',   new Float32BufferAttribute(new Float32Array(normals),   3))
  return geo
}
